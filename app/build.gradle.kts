// Read an optional Gemini API key from local.properties so it never lands in git.
// When absent, the app silently uses the deterministic rule-based parser.
val geminiApiKey: String = run {
    val props = Properties()
    val file = rootProject.file("local.properties")
    if (file.exists()) {
        file.inputStream().use { props.load(it) }
    }
    props.getProperty("GEMINI_API_KEY", "")
}

